#!/usr/bin/env bash

# export for dependant map tool, you might need to set it yourself if doesn't work
export DEVICE_MAP_FILE="../device_id_mapping/device_id_map.csv"
export DEVICE_NAME_MAPPER="../device_id_mapping/device-id-norm.sh"

if [ "$1" = "help" ] || [ "$1" = "-help" ]; then
echo "help:"
echo "(1) run this without arguments to perform tests and see summary"
echo "(2) run this with any argument different that help to process \
selftest_pcie_profile and spit to stdout perform commands to be pasted at the end of \
this test script (see current code)."
exit 0
fi

if [ -n "$1" ]; then
    skip_line=0
    while IFS=',' read -r dev id_pos cmd queryType opcode arg1 arg2 dataOutBits extDataOutBits additionalDataInCnt additionalDataBytes
    do
        if [ $skip_line -eq 0 ]; then
            skip_line=1
            continue
        fi

        if [ -n "$dataOutBits" ]; then
            bits=$dataOutBits
        elif [ -n "$extDataOutBits" ]; then
            bits=$extDataOutBits
        else
            echo "Error: profile file wrong config"
            exit 1
        fi
        left=${bits%:*}
        right=${bits#*:}
        # calc start bit, bit width and bit mask
        startBit=$right
        bitWidth=$(($left - $right + 1))
        bitMask=$(( (2 ** $bitWidth) - 1 ))
                # echo "#start $startBit width $bitWidth mask $bitMask"
        rand=$RANDOM
        expectedWrapperOut=$(( ($rand & $bitMask) ))
                # echo "#rand $rand expout $expectedWrapperOut"
        value=$(( ($RANDOM | ($RANDOM << 16)) & 0xffffffff ))
                # echo "# $(($value))"
        shiftedMask=$(($bitMask << $startBit))
                # echo "# $shiftedMask"
        value=$(( ${value} & ~$shiftedMask ))
                # echo "# $value"
        value=$(( ${value} | ($expectedWrapperOut << $startBit) ))
                # echo "# $value"

        # Randomize some value, and mask it into simulated randomized data 
        # output register or extended data output register (depending on profile
        # config). During test expect that unmasked value would equal value 
        # masked into register.

        if [ -n "$dataOutBits" ]; then
            artificialCmdOutput="(iau) 0 4 0 520093729 $value 0xdecafbad"
        elif [ -n "$extDataOutBits" ]; then
            artificialCmdOutput="(iau) 0 4 0 520093729 0xdecafbad $value"
        fi

        randId=$RANDOM 
        if [ "${dev,,}" = "gpu" ]; then
            randId=$(( ( randId % 8 ) + 1 ))
            dev="GPU_SXM_$randId"
        elif [ "${dev,,}" = "nvswitch" ]; then
            randId=$(( (randId % 4) ))
            dev="NVSwitch_$randId"
        elif [ "${dev,,}" = "pcieretimer" ]; then
            randId=$(( (randId % 8) ))
            dev="PCIeRetimer_$randId"
        elif [ "${dev,,}" = "pcieswitch" ]; then
            dev="PCIeSwitch_0"
        elif [ "${dev,,}" = "fpga" ]; then
            dev="FPGA_0"
        elif [ "${dev,,}" = "hmc" ]; then
            dev="BMC_0"
        else
            echo "unknown device (${dev})"
            exit 1
        fi

        echo "perfTest \"$cmd\" \"$dev\" \"$artificialCmdOutput\" \"$expectedWrapperOut\""
    done < "selftest_pcie_profile.csv"

    exit 0
fi

testsTotal=0
testsPassed=0
testsFailed=0

perfTest() 
{
    testsTotal=$(($testsTotal + 1))
     local symbol="$1"
     local dev="$2"
     local artOut="$3"
     local exp="$4"
    # Warning - hacky workaround here, substitute spaces to underscores then in 
    # called script restore spaces from underscores. This is because bash strips
    # quotes when evaluating cmd making problem with passing string argument eg.
    # "(iau) 0 4 0 520093729 131412 6" is "" stripped and appears as 7 args.
    artOut=${artOut//' '/'_'}
    local cmd="./pcie_wrapper -test-run $symbol $dev $artOut"
    local resp=$($cmd)

    if [ "$resp" != "$exp" ]; then
        echo "ERROR: test failed $symbol $dev resp $resp != expected $exp"
        testsFailed=$(($testsFailed + 1))
    else
        testsPassed=$(($testsPassed + 1))
    fi

    echo -n -e "test $testsTotal\r"
}

summary()
{
    echo "Tests total $testsTotal passed $testsPassed failed $testsFailed"
    if [ $testsFailed -ne 0 ]; then
        exit 1
    fi
    exit 0
}

